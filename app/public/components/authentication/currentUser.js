localLearnersApp.factory('CurrentUser', function() {
    
    return {
        create: create,
        destroy: destroy
    }

    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Public Functions
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    function create(profile) {
        this._id = profile._id;
        this.meetupProfile = profile.meetupProfile;
    }
    function destroy() {
        this._id = null;
        this.meetupProfile = null;
    }
    
});